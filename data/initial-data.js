// פונקציה לאתחול הנתונים ב-localStorage
function initializeData() {
    if (!localStorage.getItem('users')) {
        localStorage.setItem('users', JSON.stringify(INITIAL_DATA.users));
    }
    if (!localStorage.getItem('recipes')) {
        localStorage.setItem('recipes', JSON.stringify(INITIAL_DATA.recipes));
    }
}

// פונקציה לעדכון מתכונים (אם יש פחות מ-35)
function updateRecipes() {
    const currentRecipes = JSON.parse(localStorage.getItem('recipes') || '[]');
    if (currentRecipes.length < 35) {
        localStorage.setItem('recipes', JSON.stringify(INITIAL_DATA.recipes));
        console.log('מתכונים עודכנו ל-34 מתכונים!');
        return true;
    }
    return false;
}

